        sb.append("- الصف الدراسي: ").append(grade).append("\n");
        sb.append("- العمر: ").append(age).append(" سنوات\n");
        sb.append("- نقاط التفاعل المكتسبة على المنصة: ").append(points).append("\n");
        sb.append("- عدد المهام المكتملة على المنصة: ").append(missions).append("\n");
        if (classroomName != null && !classroomName.isBlank())
            sb.append("- الفصل الدراسي المسجل فيه: ").append(classroomName).append("\n");
        if (points == 0 && missions == 0)
            sb.append("\nملاحظة: لم تُسجَّل أي بيانات تفاعل للطالب على المنصة حتى الآن.\n");
