    // ── Classroom summary ────────────────────────────────────────────────────

    public ClassroomSummaryOutDTO analyzeClassroom(Integer classroomId) {
        ClassroomDashboardOutDTO dashboard = classroomService.getDashboard(classroomId);
        List<StudentProgressOutDTO> progress = classroomService.getProgress(classroomId);

        int studentCount = progress.size();
        double avgPoints   = studentCount == 0 ? 0.0
                : progress.stream().mapToInt(StudentProgressOutDTO::getTotalPoints).average().orElse(0);
        double avgMissions = studentCount == 0 ? 0.0
                : progress.stream().mapToInt(StudentProgressOutDTO::getCompletedMissionsCount).average().orElse(0);

        // Try OpenAI for text fields
        OpenAiService.AiAnalysisResult ai = openAiService.analyze(
                SYSTEM_PROMPT,
                classroomUserPrompt(dashboard.getName(), dashboard.getTeacherName(), studentCount, avgPoints, avgMissions)
        );

        String summary;
        List<String> strengths;
        List<String> concerns;
        List<String> actions;
        String source;

        if (ai != null) {
            summary   = ai.getSummary();
            strengths = safe(ai.getStrengths());
            concerns  = safe(ai.getConcerns());
            actions   = safe(ai.getRecommendedActions());
            source    = "openai";
        } else {
            strengths = new ArrayList<>();
            concerns  = new ArrayList<>();
            actions   = new ArrayList<>();
            classroomFallback(studentCount, avgPoints, avgMissions, strengths, concerns, actions);
            summary = classroomFallbackSummary(dashboard.getName(), dashboard.getTeacherName(), studentCount, avgPoints, avgMissions);
            source  = "fallback";
        }

        return new ClassroomSummaryOutDTO(
                "تحليل الفصل: " + dashboard.getName(),
                summary, strengths, concerns, actions, now(),
                dashboard.getClassroomId(), dashboard.getName(), dashboard.getTeacherName(),
                studentCount, roundHalf(avgPoints), roundHalf(avgMissions),
                source
        );
    }

    private String classroomUserPrompt(String name, String teacher, int count, double avgPts, double avgMissions) {
        return "بيانات تفاعل الفصل مع المنصة (تعكس النشاط داخل المنصة فقط، وليس الأداء الأكاديمي):\n" +
               "- اسم الفصل: " + name + "\n" +
               "- المعلم المسؤول: " + teacher + "\n" +
               "- عدد الطلاب المسجلين في المنصة: " + count + "\n" +
               "- متوسط نقاط التفاعل على المنصة: " + round1(avgPts) + "\n" +
               "- متوسط المهام المكتملة على المنصة لكل طالب: " + round1(avgMissions) + "\n\n" +
               "قدّم تحليلاً داعماً لمستوى تفاعل الطلاب مع المنصة، وتوصيات عملية للمعلم لتشجيعهم على الاستكشاف والمشاركة. " +
               "تجنب إصدار أحكام على المستوى الدراسي الحقيقي للطلاب.";
    }

    private void classroomFallback(int count, double avgPts, double avgMissions,
                                   List<String> strengths, List<String> concerns, List<String> actions) {
        if (count == 0) {
            concerns.add("لا يوجد طلاب مسجلون حاليًا في هذا الفصل.");
            actions.add("أضف الطلاب إلى الفصل للبدء في متابعة تقدمهم.");
            return;
        }
        if (avgPts >= 70)
            strengths.add("متوسط النقاط جيد (" + round1(avgPts) + ") وهذا يدل على وجود تفاعل جيد من الطلاب.");
        else if (avgPts < 30)
            concerns.add("متوسط النقاط منخفض (" + round1(avgPts) + ")، وقد يحتاج الطلاب إلى دعم إضافي.");

        if (avgMissions >= 5)
            strengths.add("متوسط إنجاز المهام " + round1(avgMissions) + " لكل طالب، وهذا يدل على نشاط مستمر.");
        else if (avgMissions < 2)
            concerns.add("متوسط إنجاز المهام منخفض (" + round1(avgMissions) + ")، مما يدل أن الطلاب لا يتقدمون في المهام بالشكل المطلوب.");

        if (concerns.isEmpty())
            actions.add("الاستمرار على نفس الخطة مع متابعة تقدم الطلاب بشكل منتظم.");
        else
            actions.add("مراجعة المواد التعليمية وتقديم دعم موجه للطلاب الأقل تقدمًا.");
    }

    private String classroomFallbackSummary(String name, String teacher, int count, double avgPts, double avgMissions) {
        if (count == 0)
            return "الفصل '" + name + "' لا يحتوي على طلاب مسجلين حتى الآن. أضف طلابًا للبدء في متابعة التقدم.";
        String level = avgPts >= 70 ? "جيد" : avgPts >= 40 ? "متوسط" : "منخفض";
        return "الفصل '" + name + "' بإشراف المعلم " + teacher + " يحتوي على " + count
                + " طالب. الأداء العام " + level
                + " بمتوسط " + round1(avgPts) + " نقطة و" + round1(avgMissions) + " مهام مكتملة لكل طالب.";
    }

